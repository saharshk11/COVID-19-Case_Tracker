  Future<void> getCases(bool page1, {String county}) async{
    Dio dio = Dio();
    Directory tempDir = await getTemporaryDirectory();
    String dataLocation = '${tempDir.path}/data.xlsx';
    var response = await dio.download('https://dshs.texas.gov/coronavirus/TexasCOVID19DailyCountyCaseCountData.xlsx', dataLocation);
    var bytes = File(dataLocation).readAsBytesSync();
    var decoder = SpreadsheetDecoder.decodeBytes(bytes);
    var table = decoder.tables['Cases by County'];

    int spreadsheetLength = 257;

    if(page1){
      _currentCasesTexasTotal = table.rows[spreadsheetLength][table.rows[spreadsheetLength].length-1].toInt().toString();
      _currentCasesTexasSinceYesterday = (table.rows[spreadsheetLength][table.rows[spreadsheetLength].length-1] - table.rows[spreadsheetLength][table.rows[spreadsheetLength].length-2]).toInt().toString();

      bool cont = true;
      for(int i = 0; i < spreadsheetLength && cont; i++){
        if(table.rows[i][0] == _currentCounty){
          _currentCasesCountyTotal = table.rows[i][table.rows[i].length-1].toInt().toString();
          _currentCasesCountySinceYesterday = (table.rows[i][table.rows[i].length-1] - table.rows[i][table.rows[i].length-2]).toInt().toString();
          cont = false;
        }
      }
      setDisplay(page1);
    } else {
      String countyCasesTotal;
      String countyCasesSinceYesterday;
      
      bool cont = true;
      for(int i = 0; i < spreadsheetLength && cont; i++){
        if(table.rows[i][0] == county){
          countyCasesTotal = table.rows[i][table.rows[i].length-1].toInt().toString();
          countyCasesSinceYesterday = (table.rows[i][table.rows[i].length-1] - table.rows[i][table.rows[i].length-2]).toInt().toString();
          cont = false;
        }
      }
      setDisplay(page1, county: county, total: countyCasesTotal, sinceYesterday: countyCasesSinceYesterday);
    }
  }

